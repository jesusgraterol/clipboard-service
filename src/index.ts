import { IClipboardService } from './types.js';

/* ************************************************************************************************
 *                                         IMPLEMENTATION                                         *
 ************************************************************************************************ */

/**
 * ClipboardService Factory
 * Generates the object in charge of interacting with the Clipboard API.
 */
const clipboardServiceFactory = (): IClipboardService => {
  /* **********************************************************************************************
   *                                          PROPERTIES                                          *
   ********************************************************************************************** */
  
  // ...
  const SOME_CONSTANT = 'I am a constant!';
  
  // ...
  let __someProperty = 123;





  /* **********************************************************************************************
   *                                            ACTIONS                                           *
   ********************************************************************************************** */

  const someAction = () => {
    // ...
  };




  /* **********************************************************************************************
   *                                         MODULE BUILD                                         *
   ********************************************************************************************** */
  return Object.freeze({
    // properties
    SOME_CONSTANT,
    get someProperty() {
      return __someProperty;
    },
    set someProperty(newSomeProperty) {
      __someProperty = newSomeProperty;
    },

    // actions
    someAction,
  });
};





/* ************************************************************************************************
 *                                        GLOBAL INSTANCE                                         *
 ************************************************************************************************ */
const ClipboardService = clipboardServiceFactory();





/* ************************************************************************************************
 *                                         MODULE EXPORTS                                         *
 ************************************************************************************************ */
export {
  // types
  type IClipboardService,

  // service
  ClipboardService,
};
